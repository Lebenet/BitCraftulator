    public static Dictionary<int, List<object>> TransformSqlToDictionary(string filePath)
    {
        Dictionary<int, List<object>> items = new Dictionary<int, List<object>>();

        using (StreamReader sr = new StreamReader(filePath))
        {
            string line;
            while ((line = sr.ReadLine()) != null)
            {
                string[] parts = line.Split('|');

                int id = int.Parse(parts[0].Trim());

                var obj = new List<object>();

                for (var i = 1; i < parts.Length; i++)
                    obj.Add(parts[i].Trim());
                
                // Add to dictionary
                items.Add(id, obj);
            }
        }

        return items;
    }
    
