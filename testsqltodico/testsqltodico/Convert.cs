    public static Dictionary<int, Dictionary<int, string>> SqlToBuildingDico(string filePath)
    {
        Dictionary<int, Dictionary<int, string>> res = new();

        using (StreamReader sr = new StreamReader(filePath))
        {
            string? l;
            while ((l = sr.ReadLine()) is not null)
            {
                string[] vars = l.Split("|");
                int type = int.Parse(vars[2].Trim());
                int level = int.Parse(vars[3].Trim());
                string name = vars[1].Trim();

                if (!res.ContainsKey(type))
                    res[type] = new Dictionary<int, string> { { level, name } };
                else
                    res[type][level] = name;
            }
        }

        return res;
    }

